class Cat():
    pass

kitty = Cat()

'''To instantiate a new object in Python, we simply call the class as if it were
a function, i.e. Cat(). This creates a new instance of the Cat class.

Once we have our Cat object, we can assign it to a variable to interact with it. 
Above we assign the new instance to the variable kitty.

This variable can be now used to interact with and manipulate the Cat object.'''